public class Divisao {

    private int divisao;

    public Divisao(int divisao) {
        this.divisao = divisao;
    }

    public int calcular(int a, int b) {
       
        return a / b;
    }

    public int getDivisao() {
        return divisao;
    }

    public void setDivisao(int divisao) {
        this.divisao = divisao;
    }

}
